/*Get user to input two number using prompt and print their possible arithmetic result*/

let a = Number(prompt("Enter Number From 1 to 300"));
let b = Number(prompt("Enter Number from 1 to 300"));
console.log("a+b=", a + b);
console.log("a-b=", a - b);
console.log("a/b=", a / b);
console.log("a*b=", a * b);
console.log("a%b=", a % b);